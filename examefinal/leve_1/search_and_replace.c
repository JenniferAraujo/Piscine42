#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	char	ponte;
	
	i = 0;
	if(ac == 4)
	{
		while(av[1][i] != '\0')
		{
			if(av[1][i] == av[2][0])
			{
				ponte = av[3][0];
				av[1][i] = ponte;
			}
			write(1, &av[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
