#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	l;
	
	i = 0;
	if (ac == 2)
	{
		while(av[1][i] != '\0')
		{
			if (av[1][i] <= 32 && av[1][i = 1] > 32)
			l = &av[1][i + 1];
		i++;
		}
		i = 0;
		while (l && l[1] > 32)
		{
			write(1, &l[i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
